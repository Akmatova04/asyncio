# Керектүү модулдарды импорттоо
import asyncio
import time # Убакытты көрсөтүү үчүн (кошумча)

# Асинхрондук таймер функциясы (корутина)
async def асинхрондук_таймер(кечигүү_секунд):
    """
    Бул асинхрондук функция (корутина) берилген 'кечигүү_секунд'
    убакыт өткөндөн кийин билдирүү чыгарат.
    """
    учурдагы_убакыт = time.strftime("%H:%M:%S") # Учурдагы убакытты форматтоо
    print(f"Таймер башталды [{учурдагы_убакыт}]. {кечигүү_секунд} секунд күтөбүз...")

    # await asyncio.sleep(кечигүү_секунд) - бул жерде программа
    # 'кечигүү_секунд' бою асинхрондук түрдө "уктайт".
    # Башка асинхрондук тапшырмалар бул убакта иштей алат.
    await asyncio.sleep(кечигүү_секунд)

    учурдагы_убакыт_кийин = time.strftime("%H:%M:%S") # Кечигүүдөн кийинки убакыт
    print(f"\nТаймер аяктады! [{учурдагы_убакыт_кийин}]")
    print(f"{кечигүү_секунд} секунд өттү.")

# Негизги асинхрондук функция (корутина)
async def main():
    """
    Бул негизги асинхрондук функция 'асинхрондук_таймер' корутинасын
    5 секунддук кечигүү менен чакырат.
    """
    күтүү_убактысы = 5
    await асинхрондук_таймер(күтүү_убактысы)
    print("Программа аяктады.")

# Программа түздөн-түз иштетилгенде
if __name__ == "__main__":
    # asyncio.run() функциясы 'main' асинхрондук функциясын ишке киргизет.
    asyncio.run(main())